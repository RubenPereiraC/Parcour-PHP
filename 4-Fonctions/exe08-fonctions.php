/*
Faire une fonction qui prend en paramètre trois nombres et
 qui retourne la somme de ces nombres. Tous les paramètres doivent avoir une valeur par défaut.
 */

function calculer($n1, $n2, $n3) {
    $n1 = 0;
    $n2 = 0;
    $n3 = 0;
